#include <stdio.h>
#include "3-calc.h"
/**
 *  * op_add -Function for addition
 *   * @a: Integer
 *    * @b: Integer
 *     * Return: Return addition result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *  * op_sub -Function for subtraction
 *   * @a: Integer
 *    * @b: Integer
 *     * Return: Return subtraction result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *  * op_mul -Function for multiplication
 *   * @a: Integer
 *    * @b: Integer
 *     * Return: Return multiplication result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *  * op_div -Function for division
 *   * @a: Integer
 *    * @b: Integer
 *     * Return: Return division result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod -Function for modulus
 * @a: Integer
 * @b: Integer
 * Return: Return modulus result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
